# Program to add two numbers

# Get input from user
number1 = int(input("Enter first number: ")) 
number2 = int(input("Enter second number: "))

total = number1 + number2 

"""
format() function is a built-in function that allows you to format and print strings.
format(value, format_spec='')
"""
print("Sum of {} and  {} is: {}".format(number1, number2, total)) 

